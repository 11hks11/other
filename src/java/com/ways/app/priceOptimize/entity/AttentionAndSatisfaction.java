package com.ways.app.priceOptimize.entity;

public class AttentionAndSatisfaction {

	private String configTypeId;
	private String myAttentionVale;  //本品关注度
	private String mySatisfactionVale;  //本品满意度
	private String vsSatisfactionVale;  //竞品满意度
	private String configTypeName;  //配置类型名称
	public String getConfigTypeId() {
		return configTypeId;
	}
	public void setConfigTypeId(String configTypeId) {
		this.configTypeId = configTypeId;
	}
	public String getMyAttentionVale() {
		return myAttentionVale;
	}
	public void setMyAttentionVale(String myAttentionVale) {
		this.myAttentionVale = myAttentionVale;
	}
	public String getMySatisfactionVale() {
		return mySatisfactionVale;
	}
	public void setMySatisfactionVale(String mySatisfactionVale) {
		this.mySatisfactionVale = mySatisfactionVale;
	}
	public String getVsSatisfactionVale() {
		return vsSatisfactionVale;
	}
	public void setVsSatisfactionVale(String vsSatisfactionVale) {
		this.vsSatisfactionVale = vsSatisfactionVale;
	}
	public String getConfigTypeName() {
		return configTypeName;
	}
	public void setConfigTypeName(String configTypeName) {
		this.configTypeName = configTypeName;
	}
	
	
	
}
