public class VsVersion {

	private String vsVersionId;
	private String vsVersionName;
	private String loseStr;
	private String advantageousStr;
	private String disadvantagedStr;

	public String getVsVersionId() {
		return vsVersionId;
	}

	public void setVsVersionId(String vsVersionId) {
		this.vsVersionId = vsVersionId;
	}

	public String getVsVersionName() {
		return vsVersionName;
	}

	public void setVsVersionName(String vsVersionName) {
		this.vsVersionName = vsVersionName;
	}

	public String getLoseStr() {
		return loseStr;
	}

	public void setLoseStr(String loseStr) {
		this.loseStr = loseStr;
	}

	public String getAdvantageousStr() {
		return advantageousStr;
	}

	public void setAdvantageousStr(String advantageousStr) {
		this.advantageousStr = advantageousStr;
	}

	public String getDisadvantagedStr() {
		return disadvantagedStr;
	}

	public void setDisadvantagedStr(String disadvantagedStr) {
		this.disadvantagedStr = disadvantagedStr;
	}
	
}
